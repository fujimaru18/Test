    public void loadTable() {
        supplierList = sup_ctr.getAll();
        model = (DefaultTableModel) tblSupplier.getModel();
        model.setRowCount(0);
        try {

            for (Supplier s : supplierList) {
                model.addRow(new Object[]{
                    s.getsupplierId(),
                    s.getName(),
                    s.getPhone_number(),
                    s.getAddress(),
                    s.getEmail()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

